    public void LerParaTabelaPorModelo(String modeloForn) throws SQLException {

        DefaultTableModel model = (DefaultTableModel) tblListaFornecedores.getModel();
        model.setNumRows(0);

        FornecedorDAO fDAO = new FornecedorDAO();
        
        for(Fornecedor forn : fDAO.bucaPorNFornecedor(modeloForn)){
            
            model.addRow(new Object[]{
            forn.getId_fornecedor(),
            forn.getNomeF(),
            forn.getEmailF(),
            forn.getTelefoneF(),
            
          
        });
        }        
    }

