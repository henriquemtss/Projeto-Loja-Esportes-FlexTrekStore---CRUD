    private void alterarFornecedor(){
               try{
           Fornecedor fornA = new Fornecedor();
            
            int id_fornecedorA;
            String nomeFA, emailA, telefoneA;
            
            id_fornecedorA = Integer.parseInt(c_idF.getText());
            nomeFA = c_nomeF.getText();
            emailA = c_emailF.getText();
            telefoneA = c_telefoneF.getText();
            
            fornA.setId_fornecedor(id_fornecedorA);
            fornA.setNomeF(nomeFA);
            fornA.setEmailF(emailA);
            fornA.setTelefone(telefoneA);
            
            FornecedorDAO fdao = new FornecedorDAO();
            
            fdao.alterarFornecedor(fornA);
            
            JOptionPane.showMessageDialog(null, "Fornecedor alterado com sucesso!");
            
       }catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Erro ao alterar!");
            System.out.println(e.getMessage());
        }
    }
    
    private void excluirFornecedor(){
        try{
            
            int id_fornecedorE;
            
            id_fornecedorE = Integer.parseInt(c_idF.getText());
            
            Fornecedor fornE = new Fornecedor();
            fornE.setId_fornecedor(id_fornecedorE);
            
            FornecedorDAO fdao = new FornecedorDAO();
            fdao.excluirFornecedor(fornE);
            
            JOptionPane.showMessageDialog(null, "Fornecedor excluido com sucesso!");
        }catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Erro ao excluit!");
            System.out.println(e.getMessage());
        }
        
    }
